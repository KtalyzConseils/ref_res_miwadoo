function starsHTML(rating) {
  const full = Math.floor(rating);
  const half = rating % 1 >= 0.5;
  let h = '★'.repeat(full);
  if (half) h += '½';
  h += '☆'.repeat(5 - full - (half ? 1 : 0));
  return h;
}

